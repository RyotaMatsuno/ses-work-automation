# ===== Drive版基準値 =====
drive_base = {
    6: 787202,  # 4月稼働
    7: 825282,  # 5月稼働（鶴川含む）
    8: 811262,  # 6月稼働
    9: 811262,  # 7月稼働（仮）
}

# ===== 追加分（Drive版に含まれていないもの）=====
additions = {
    6: 0,  # 4月稼働分（吉田・鶴見・遠藤なし）
    7: 50000,  # 5月稼働分: 鶴見6月稼働→7月入金 +50,000（手取り）
    8: 50000 + 39508 + 26400,  # 6月稼働分: 鶴見+吉田TERRA+吉田FT
    9: 50000 + 39508 + 26400 + 18700,  # 7月稼働分: 鶴見+吉田TERRA+吉田FT+遠藤
}

# ===== 源泉徴収（確定申告で還付）=====
# TERRA税抜請求 × 10.21% = 源泉
# Drive版の源泉(月間総額)：
gensen = {
    6: 48498,  # Drive版より
    7: 48498,  # Drive版より
    8: 49518,  # 吉田TERRA(税抜4万×10.21%=4,492)含む
    9: 49518,  # 同上
}
# 吉田TERRAの源泉は8月以降のadditionsの中に含めた39,508円が源泉控除後の数字なので、
# 額面に戻す時は+4,492円を別途加算する必要がある

# === 出力 ===
print("=" * 75, flush=True)
print("【入金月別 実入り（手取り）と額面（TERRA源泉込み）】", flush=True)
print("=" * 75, flush=True)

for m in [6, 7, 8, 9]:
    tedori = drive_base[m] + additions[m]
    # 額面 = 手取り + Drive版TERRA源泉 + 吉田TERRA源泉(8月以降)
    if m >= 8:
        gengaku = tedori + gensen[m]  # 既に49,518円に吉田分4,492円が含まれているはず
        # ただし吉田TERRA分は手取り39,508円(税込44,000-源泉4,492)で計算しているので、源泉戻すと44,000
        # gensen[m]=49,518にすでに吉田分が含まれていると整合
    else:
        gengaku = tedori + gensen[m]

    print(f"\n■ {m}月入金", flush=True)
    print(f"  Drive版基準: {drive_base[m]:,}円", flush=True)
    print(f"  追加分: +{additions[m]:,}円", flush=True)
    print(f"  → 手取り合計: {tedori:,}円", flush=True)
    print(f"  → 額面(源泉込み): {gengaku:,}円", flush=True)

print("\n" + "=" * 75, flush=True)
print("【追加分の内訳】", flush=True)
