# Drive版に鶴川分が含まれているか？
# 過去チャット5/19の更新で「鶴川分入金予測に反映するは未確認」と記載されていた
# 5/29チャットの計算: 8月入金にFT 375,200=鶴川47,600込みの数字に見える
