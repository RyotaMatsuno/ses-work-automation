# ===== 定義 =====
# 入金月の判定:
# 稼働月=5月として:
#   サイト30日=翌月末=6月末
#   サイト40日=翌々月10日=7月10日
#   サイト45日=翌々月15日=7月15日(or8/15)
#   サイト50日=翌々月20日=7月20日
#   サイト55日=翌々月末=7月末
#   直契約=翌月最終営業日


# ===== 稼働月別の入金月マッピング =====
# 稼働月 → 入金月（サイト別）
def get_nyukin_month(work_month, site):
    """work_month: 7,8など。site: 30,40,45,50,55など"""
    if site <= 30:
        return work_month + 1
    elif site <= 45:
        return work_month + 2
    elif site <= 55:
        return work_month + 2
    else:
        return work_month + 2


