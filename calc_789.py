month_totals = {7: 0, 8: 0, 9: 0}
month_details = {7: [], 8: [], 9: []}

for name, tanto, category, jissyu, site, kihara, note in workers:
    # 7月入金 = 5月稼働分
    # 8月入金 = 6月稼働分
    # 9月入金 = 7月稼働分
    for work_m, nyukin_m in [(5, 7), (6, 8), (7, 9)]:
        nm = get_nyukin_month(work_m, site)
        # サイト別の実際の入金月を計算
        # 直契約は翌月末なのでwork_m+1
        if category == "直契約":
            nm = work_m + 1
        if nm == nyukin_m:
            # 7月〜の新規は6月以前稼働分には含めない
            is_new_july = "7月" in note or "遠藤" in name or "吉田祥平" in name
            if is_new_july and work_m < 7:
                continue
            month_totals[nyukin_m] += jissyu
            month_details[nyukin_m].append((name, tanto, jissyu, site, note))

